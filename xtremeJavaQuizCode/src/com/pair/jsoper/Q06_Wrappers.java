package com.pair.jsoper;

public class Q06_Wrappers {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		Integer i1 = 50;
		Integer i2 = 50;
		System.out.println("For wrapped integers:");
		System.out.println(i1 + " == " + i2 + " is " + (i1 == i2));
		
		Integer i3 = 100;
		Integer i4 = 100;
		System.out.println(i3 + " == " + i4 + " is " + (i3 == i4));

		Integer i5 = 128;
		Integer i6 = 128;
		System.out.println(i5 + " == " + i6 + " is " + (i5 == i6));
	}

}
